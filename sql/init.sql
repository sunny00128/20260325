BEGIN
    PRINT 'Table fact already exists.';
END
GO

-- ============================================================
-- Table: item (商品資料)
-- ============================================================
IF NOT EXISTS (
    SELECT * FROM sys.tables WHERE name = 'item'
)
BEGIN
    CREATE TABLE item (
        item_code  NVARCHAR(10)  NOT NULL PRIMARY KEY,
        item_name  NVARCHAR(100) NOT NULL,
        fact_code  NVARCHAR(10)  NULL,
        CONSTRAINT FK_item_fact FOREIGN KEY (fact_code) REFERENCES fact(fact_code)
    );
    PRINT 'Table item created.';
END
ELSE
BEGIN
    PRINT 'Table item already exists.';
END
GO

